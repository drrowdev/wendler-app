export const LEADER_TEMPLATES = WENDLER_TEMPLATES.filter((t) => t.blockKind === 'leader');
export const ANCHOR_TEMPLATES = WENDLER_TEMPLATES.filter((t) => t.blockKind === 'anchor');
export const STANDALONE_TEMPLATES = WENDLER_TEMPLATES.filter((t) => t.blockKind === 'standalone');

export const SUPPORTED_TEMPLATES = WENDLER_TEMPLATES.filter(
  (t) => t.supplementalTemplate !== 'unsupported',
);
export const UNSUPPORTED_TEMPLATES = WENDLER_TEMPLATES.filter(
  (t) => t.supplementalTemplate === 'unsupported',
);

export function getTemplateById(id: string): WendlerTemplate | undefined {
  return WENDLER_TEMPLATES.find((t) => t.id === id);
}

export function getTemplatesByScheme(scheme: MainScheme): WendlerTemplate[] {
  return WENDLER_TEMPLATES.filter((t) => t.mainScheme === scheme);
}

export function getTemplatesBySupplemental(
  supp: SupplementalTemplateId | 'unsupported',
): WendlerTemplate[] {
  return WENDLER_TEMPLATES.filter((t) => t.supplementalTemplate === supp);
}

export function getTemplatesByConditioningLoad(
  compat: ConditioningCompatibility,
): WendlerTemplate[] {
  return WENDLER_TEMPLATES.filter((t) => t.conditioningCompatibility === compat);
}

export function sortByCnsLoad(
  templates: WendlerTemplate[] = WENDLER_TEMPLATES,
  direction: 'asc' | 'desc' = 'asc',
): WendlerTemplate[] {
  const factor = direction === 'asc' ? 1 : -1;
  return [...templates].sort(
    (a, b) => factor * (CNS_LOAD_RANK[a.cnsLoad] - CNS_LOAD_RANK[b.cnsLoad]),
  );
}

