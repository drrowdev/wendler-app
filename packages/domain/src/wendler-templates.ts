// reference the source.

import type { MainScheme } from './waves';
import type { SupplementalTemplateId } from './supplemental';
import type { BlockKind } from './blocks';

export type CnsLoad = 'low' | 'moderate' | 'high' | 'very-high';
export type ConditioningCompatibility = 'incompatible' | 'low' | 'medium' | 'high';
export type HypertrophyTier = 'low' | 'moderate' | 'high' | 'very-high';
export type StrengthTier = 'low' | 'moderate' | 'high' | 'very-high';
export type Audience = 'beginner' | 'intermediate' | 'advanced' | 'all-levels';

export interface WendlerTemplate {
  id: string;
  name: string;
  bookPage: string;
  summary: string;
  blockKind: BlockKind;
  mainScheme: MainScheme;
  mainSchemeNote?: string;
  supplementalTemplate: SupplementalTemplateId | 'unsupported';
  supplementalNote?: string;
  amrap: 'always' | 'anchor-only' | 'never' | 'pr-set-only';
  cnsLoad: CnsLoad;
  strengthTier: StrengthTier;
  hypertrophyTier: HypertrophyTier;
  conditioningCompatibility: ConditioningCompatibility;
  daysPerWeek: number[];
  /** Tuple of [easyDays, hardDays] of conditioning the template tolerates. */
  conditioningDaysEasy: [number, number];
  conditioningDaysHard: [number, number];
  recommendedDurationBlocks: { min: number; max: number };
  audience: Audience;
  goalEmphasis: string[];
  cautions: string[];
  /** Successor template ids when the book recommends a Leader→Anchor pairing. */
  pairsWith?: string[];
}

